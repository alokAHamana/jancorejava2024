package com.aa.flowcontrol;

public class Array_forloop{        //........

	public static void main(String[]args) {
	        
	        int [] a = new int [5];
	        
	        a [0] = 50;
	        a [1] = 60;
	        a [2] = 70;
	        a [3] = 80;
	        a [4] = 90;
	         
	        int i=0;
	        
	        while(i<a.length) {
	            
	            System.out.println(a[i]);
	            i++;
	        }
	    }
	}
